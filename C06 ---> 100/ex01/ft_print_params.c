#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	c;

	c = 1;
	i = 0;
	while (ac > c)
	{
		write(1, &av[c][i], 1);
		i++;
		if (av[c][i] == 0)
		{
			write(1, "\n", 1);
			c++;
			i = 0;
		}
	}
}
