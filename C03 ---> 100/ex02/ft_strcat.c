char	*ft_strcat(char *dest, char *src)

{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (dest[i] != '\0')
		i++;
	while (src[a] != '\0')
	{
		dest[a + i] = src[a];
		a++;
	}
	dest[a + i] = '\0';
	return (dest);
}
